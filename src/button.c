#include "button.h"

#include <zephyr/kernel.h>
#include <zephyr/device.h>
#include <zephyr/devicetree.h>
#include <zephyr/drivers/gpio.h>

#define BUTTON_NODE DT_ALIAS(sw0)

static const struct gpio_dt_spec button = GPIO_DT_SPEC_GET(BUTTON_NODE, gpios);

void button_init(void)
{
    gpio_pin_configure_dt(&button, GPIO_INPUT | GPIO_PULL_UP | GPIO_ACTIVE_LOW);
}

void button_get_rpc(const rpc_request_t *request, rpc_response_t *response)
{
    int state = gpio_pin_get_dt(&button);

    rpc_response_prepare_result(response);

    cbor_encode_boolean(&response->encoder, state);

    rpc_response_send(response);
}